#!/usr/bin/env python3

# Exercise 05: Distance traveled

# Constant for the speed of the car (70 kilometer per hour)
SPEED = 70

# Calculate the distance for 6 hours
distance_six_hours = SPEED * 6

# Calculate the distance for 10 hours
distance_ten_hours = SPEED * 10

# Calculate the distance for 15 hours
distance_fifteen_hours = SPEED * 15

# Display the results
print(f"The distance after 6 hours: {distance_six_hours} kilometer")
print(f"The distance after 10 hours: {distance_ten_hours} kilometer")
print(f"The distance after 15 hours: {distance_fifteen_hours} kilometer")
